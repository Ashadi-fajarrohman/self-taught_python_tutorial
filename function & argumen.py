# fungsi menggunakan argumen
def mahasiswa(nama):
    print('Nama siswa   :',nama)
mahasiswa('ashadi fajarrohman')

# fungsi keyword aargumen
def NIM(nomor):
    print('NIM          :',nomor)
NIM('19106020027')

def status(prodi,semester):
    print('program studi:',prodi)
    print('semester     :',semester)
status(prodi='fisika',semester='2')

